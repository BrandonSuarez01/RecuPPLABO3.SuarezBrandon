class Anuncio
{
    constructor(id, titulo, descripcion, animal, precio, desparacitado, pedigree)
    {
        this.id=id;
        this.titulo = titulo;
        this.descripcion=descripcion;
        this.animal=animal;
        this.precio=precio;
        this.desparacitado=desparacitado;
        this.pedigree=pedigree;
    }
}

export default Anuncio;